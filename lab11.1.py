import pandas as pd
# Початковий словник
magazines = {
    "Наука і життя": {"ціна": 85.0, "тираж": 9500, "категорія": "наука"},
    "Мандрівник": {"ціна": 60.5, "тираж": 12000, "категорія": "подорожі"},
    "Світ природи": {"ціна": 78.2, "тираж": 8000, "категорія": "природа"},
    "IT Сьогодні": {"ціна": 95.3, "тираж": 15000, "категорія": "технології"},
    "Кулінарія+": {"ціна": 50.0, "тираж": 7000, "категорія": "кулінарія"},
    "Мистецтво і ми": {"ціна": 105.0, "тираж": 5000, "категорія": "мистецтво"},
    "Бізнес PRO": {"ціна": 120.0, "тираж": 11000, "категорія": "економіка"}
}
# Перетворення словника на DataFrame
df = pd.DataFrame.from_dict(magazines, orient='index')
# Виведення вмісту DataFrame
print("Повний DataFrame:")
print(df, "\n")
# 1. Перші 3 рядки
print("Перші 3 рядки DataFrame:")
print(df.head(3), "\n")
# 2. Типи даних
print("Типи даних:")
print(df.dtypes, "\n")
# 3. Кількість рядків і стовпців
print("Розмірність DataFrame (рядки, стовпці):")
print(df.shape, "\n")
# 4. Описова статистика
print("Описова статистика:")
print(df.describe(), "\n")
# 5. Додавання нового стовпця (загальна вартість продажу)
df["загальна_вартість"] = df["ціна"] * df["тираж"]
print("DataFrame після додавання стовпця 'загальна_вартість':")
print(df, "\n")
# 6. Фільтрація — товари з ціною понад 10 000 грн
filtered_df = df[df["загальна_вартість"] > 10000]
print("Журнали із загальною вартістю понад 10 000 грн:")
print(filtered_df, "\n")
# 7. Сортування за спаданням ціни
sorted_df = df.sort_values(by="ціна", ascending=False)
print("Сортування за спаданням ціни:")
print(sorted_df, "\n")
# 8. Групування за категорією та обчислення середнього значення ціни
grouped_mean = df.groupby("категорія")[["ціна", "тираж", "загальна_вартість"]].mean()
print("Середні значення за категорією:")
print(grouped_mean, "\n")
# 9. Максимальна сума продажів у категорії
max_sales = df.groupby("категорія")["загальна_вартість"].max()
print("Максимальна сума продажів у кожній категорії:")
print(max_sales, "\n")
# 10. Кількість унікальних журналів
unique_count = df.index.nunique()
print("Кількість унікальних журналів:", unique_count)